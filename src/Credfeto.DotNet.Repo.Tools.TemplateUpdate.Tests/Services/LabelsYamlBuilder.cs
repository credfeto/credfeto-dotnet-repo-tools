using System.Collections.Generic;
using System.Text;

namespace Credfeto.DotNet.Repo.Tools.TemplateUpdate.Tests.Services;

internal sealed class LabelsYamlBuilder
{
    private readonly List<(string Name, string Color, string Description)> _labels = [];

    public LabelsYamlBuilder Add(string name, string color, string description)
    {
        this._labels.Add((name, color, description));

        return this;
    }

    public string Build()
    {
        StringBuilder labelsYaml = new StringBuilder()
            .AppendLine("--- # Defines the labels available in this GitHub repository.")
            .AppendLine("# Generated by credfeto-dotnet-repo-tools");

        bool first = true;

        foreach ((string name, string color, string description) in this._labels)
        {
            if (!first)
            {
                labelsYaml = labelsYaml.AppendLine();
            }

            first = false;

            labelsYaml = labelsYaml.AppendLine($"- name: \"{name}\"").AppendLine($"  color: \"{color}\"");

            if (!string.IsNullOrWhiteSpace(description))
            {
                labelsYaml = labelsYaml.AppendLine($"  description: \"{description}\"");
            }
        }

        return labelsYaml.ToString();
    }
}
